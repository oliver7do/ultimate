<?php
namespace Model\Entity;

class Category extends BaseEntity
{ 
    private $type;    


    /**
     * Get the value of type
     */ 
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set the value of type
     *
     * @return  self
     */ 
    public function setType($type)
    {
        $this->type = $type;

        return $this;    
    }
}